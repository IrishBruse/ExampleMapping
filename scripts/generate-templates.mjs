#!/usr/bin/env node
// scripts/generate-templates.mjs
// Reads markdown template files from templates/ and generates
// src/generated/templates.ts with their contents embedded as strings.

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const templatesDir = join(root, "templates");
const outDir = join(root, "src", "generated");
const outFile = join(outDir, "templates.ts");

const names = ["agentsmd", "story", "rule", "example", "question"];

function readTemplate(name) {
    return readFileSync(join(templatesDir, `${name}.md`), "utf-8");
}

function escapeForTemplateLiteral(str) {
    return str.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

const parts = [
    "// Auto-generated by scripts/generate-templates.mjs — do not edit by hand.",
    "",
];

for (const name of names) {
    const varName = `${name.toUpperCase()}_TEMPLATE`;
    const content = readTemplate(name);
    parts.push(`export const ${varName} = \`${escapeForTemplateLiteral(content)}\`;`);
    parts.push("");
}

mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, parts.join("\n"), "utf-8");
console.log(`Generated ${outFile} (${names.length} templates)`);
